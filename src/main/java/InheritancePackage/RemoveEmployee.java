    private void RemoveIENKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_RemoveIENKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER){
        SearchForRemove(RemoveIEN.getText().trim());
        if (RemoveIEN.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "FILL IN ALL FIELDS!");
        } else if (found == true) {
            RemoveEmployee(RemoveIEN.getText().trim());
            theHT.removeFromTable(Integer.parseInt(RemoveIEN.getText().trim()));
            JOptionPane.showMessageDialog(null, "Removed Employee");
            theHT.display();
            RemoveIEN.setText("");
            found = false;
        } else {
            JOptionPane.showMessageDialog(null, "Employee number not in database!");
        }
        }
    }//GEN-LAST:event_RemoveIENKeyPressed

