    public void RemoveEmployee(String s){
        try{
        File inputFile = new File(LP.fileName);
        File tempFile = new File("EmployeeTemp.txt");

        BufferedReader reader = new BufferedReader(new FileReader(inputFile));
        BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));

        String lineToRemove = s;
        String currentLine;
        while ((currentLine = reader.readLine()) != null) {
            String[] parts = currentLine.split(",");
              
            if (parts[1].equals(lineToRemove)) {
                continue;
            }
            
            writer.write(currentLine + "\n");
        }
        writer.close();
        reader.close();
        inputFile.delete();
        tempFile.renameTo(inputFile);
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex.getMessage()); ;

        }
    }
    public void SearchForRemove(String s){

         String fileName = LP.fileName;

       try {
            FileReader reader = new FileReader(fileName);
            BufferedReader br = new BufferedReader(reader); 

            String line;

            while ((line = br.readLine()) != null) {
               String[] parts = line.split(",");
                if (parts[1].equals(s)){
                    found =  true;
            }
            }
        reader.close();
        br.close();
       }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage()); ;
        }
    }    
